#include "push_swap.h"

long	ft_atoi(const char *str)
{
	long	a;
	int		b;
	int		i;

	a = 0;
	b = 1;
	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		b = b * (-1);
		i++;
	}
	if (str[i] == '\0')
		return (2147483649);
	while (str[i] >= 48 && str[i] <= 57 && a < 2147483649)
		a = a * 10 + str[i++] - 48;
	if (str[i] != '\0')
		return (2147483649);
	return (a * b);
}
